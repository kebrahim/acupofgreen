      <h1>What is Acupuncture?&nbsp;&nbsp;&nbsp;&nbsp;<a href="#home">(back to top)</a></h1>
      <p>
      Acupuncture is one of the oldest, most commonly used medical procedures in the world. Originating in China more than 2,000 years ago,
      acupuncture involves the placement of hair-thin needles into the skin to stimulate specific anatomic points in the body for therapeutic
      purposes. The acupuncture points are stimulated to balance the movement of energy (called "Qi", pronounced "chee") in the body to restore
      health and balance. A fundamental principle of acupuncture is to treat the whole person -- body, mind, and spirit -- and to relieve the
      root cause of disease, not just the symptoms.
      </p>
      
      <a name="howwork"></a>
      <h1>How does Acupuncture work?&nbsp;&nbsp;&nbsp;&nbsp;<a href="#home">(back to top)</a></h1>
      <p>
      Chinese Medicine considers illness as a message to the body to make a change. And an acupuncture needle communicates directly with
      the body, indicating that there is an imbalance and inviting a change. Acupuncture is based on the premise that Qi moves along the
      channels of the body. Normally, Qi circulates along these meridians and enables all the organs of the body to function properly and
      the emotions to be in balance. When the Qi is obstructed, pain or disease may result. Healing can be achieved by needling these obstructed
      points.
      </p>
      
