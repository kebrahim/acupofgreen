      <a name="safe"></a>
      <h1>Is Acupuncture safe?&nbsp;&nbsp;&nbsp;&nbsp;<a href="#home">(back to top)</a></h1>
      <p>
      Acupuncture is safe and without any serious side effects. The U.S. Food and Drug Administration approved acupuncture needles for use
      by licensed practitioners in 1996. The FDA requires that all needles be sterile and single-use. Very few complications from the use
      of acupuncture have been reported to the FDA, despite the millions of people treated each year.
      </p>
      
      <a name="treatments"></a>
      <h1>How many treatments will I need?&nbsp;&nbsp;&nbsp;&nbsp;<a href="#home">(back to top)</a></h1>
      <p>
      Acupuncture addresses the underlying cause of a problem rather than fixing a symptom, and therefore can take time to have an effect.
      While some people may experience immediate results, most notice improvement within three to five treatments. Of course, results can
      vary from person to person.
      </p>
      
      <a name="western"></a>
      <h1>What is the relationship between Acupuncture and contemporary Western Medicine?&nbsp;&nbsp;&nbsp;&nbsp;<a href="#home">(back to top)</a></h1>
      <p>
      Acupuncture can be extremely beneficial as a "complementary" medicine. Although it is often used alone, it is easily integrated into
      and used with Western Medicine. Although Chinese Medicine explains acupuncture in its own terms, (Yin/Yang, Qi, etc), in Western
      terms acupuncture may cause a release of endorphins in the body. In addition, acupuncture needles may increase the amount of blood
      flow in the area around the needle. The increased blood flow can supply additional nutrients or remove toxic substances, or
      both, promoting healing.
      </p>

